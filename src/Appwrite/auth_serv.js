/**               Auth service of Appwrite               */
/**              See Docs for more info :                  */

import config from "../config/config";
import { Client, Account, ID } from "appwrite";

export class AuthService {
  client = new Client();
  account;

  constructor() {
    this.client
      .setEndpoint(config.appwriteUrl)
      .setProject(config.appwriteProjectId);
    this.account = new Account(this.client);
  }

  async createAccount({ email, password, name }) {
    try {
      const userAccount = await this.account.create(
        ID.unique(),
        email,
        password,
        name,
      );
      console.log(`Creating user account `)
      if (userAccount) {
        // login or show user
        return this.login({ email, password });
      } else {
        return userAccount;
      }
    } catch (error) {
      throw error;
    }
  }

  async login({ email, password }) {
    try {
      return await this.account.createEmailPasswordSession(email, password);
    } catch (error) {
      throw error;
    }
  }

  async currentUserStatus() {
    try {
      return await this.account.get();
      // user ? (console.log(user)) : (console.log('problem in fetching current user status'))
    } catch (error) {
      console.log("Appwrite service :: currentUserStatus :: error", error);
    }

    /** if account not find (get()) */
    return null;
  }

  async logout() {
    try {
      await this.account.deleteSessions();
    } catch (error) {
      console.log("Appwrite service :: logout :: error", error);
    }
  }
}

const authService = new AuthService();

export default authService;
